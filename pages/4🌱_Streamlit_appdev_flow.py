import streamlit as st

st.set_page_config(
    page_title="Streamlit 앱 만들기 한눈에 보기",
    layout="wide"
)

st.title("🧭 Streamlit 앱 만들기: 한눈에 보는 전체 흐름")
st.caption("초보자를 위한 단계별 안내 · 전공 수업 활용을 염두에 둔 설명")

# -----------------------------
# Tabs
# -----------------------------
tab1, tab2, tab3, tab4, tab5 = st.tabs(
    [
        "① 준비 단계",
        "② 코드 작성",
        "③ 앱 실행",
        "④ 배포하기",
        "⑤ 수업 활용 아이디어",
    ]
)

# -----------------------------
# Tab 1: 준비 단계
# -----------------------------
with tab1:
    st.header("① 준비 단계 (Getting Ready): 무엇이 필요할까?")

    st.markdown("""
    Streamlit 앱을 만들기 위해서는 **아주 기본적인 준비만** 있으면 된다.

    ### ✔ 필요한 것
    - **구글 계정**: Google Colab(구글 코랩) 사용 시 필요 (Optional)
    - **GitHub 계정**: 만든 앱을 저장하고 공유하기 위해 사용
    - **Streamlit 계정**: 앱을 배포하는 곳 (Hugging Face Spaces에도 가능)
    - **웹 브라우저**: 크롬(Chrome)이나 엣지(Edge)면 충분

    📌 참고: streamlit 계정과 Github 계정을 연동시켜 두면 편하다.

    ### ✔ 미리 알아두면 좋은 개념
    - **파이썬(Python)**: Streamlit 앱을 만드는 데 사용하는 언어  
      → 문법을 깊게 알 필요는 없고, *예제를 수정하며 익히거나 처음에는 생성형 인공지능 도구를 활용하여 원하는 코드를 얻어서 사용해도 충분*
    - **웹 앱(Web App)**: 설치 없이 브라우저에서 바로 실행되는 프로그램

    👉 이 단계의 목표는  
    **“내가 앱을 만들 수 있는 환경에 들어왔다”는 감각을 갖는 것**이다.
    
    """)

# -----------------------------
# Tab 2: 코드 작성
# -----------------------------
with tab2:
    st.header("② 코드 작성 (Coding): 앱의 내용을 만든다")

    st.markdown("""
    Streamlit 앱은 **파이썬 코드 한 파일**로 시작한다.  
    이 파일 안에 *화면에 보일 내용*과 *사용자와의 상호작용*을 적는다.
    
    📌 참고: 깃허브(Github)에서 파일이름.py로 새로운 파일을 만들고 그 안에 코드를 작성한다.

    ### ✔ 가장 기본적인 구조
    ```python
    import streamlit as st

    st.title("나의 첫 Streamlit 앱")
    st.write("이것은 웹에서 실행되는 앱입니다.")
    ```

    ### ✔ 자주 쓰는 구성 요소 (코드 예시: st는 streamlit 문법 코드라는 의미로 이해하면 됨.)
    - `st.title()` : 페이지 제목
    - `st.write()` : 설명 글
    - `st.button()` : 버튼
    - `st.selectbox()` : 선택형 질문
    - `st.tabs()` : 여러 화면을 단계별로 나누기

    ### ✔ 중요한 특징
    - **HTML, CSS를 몰라도 됨**
    - 화면을 만드는 코드와 로직이 *한 곳에 있음*
    - 수정 → 저장 → 바로 결과 확인 가능

    👉 이 단계의 핵심은  
    **“코드 = 수업용 자료를 구성하는 설계도”라는 인식**이다.
    
    """)

# -----------------------------
# Tab 3: 앱 실행
# -----------------------------
with tab3:
    st.header("③ 앱 실행 (Run): 실제로 화면을 확인한다")

    st.markdown("""
    코드를 깃허브(Github)에서 파일(.py)로 작성했다면, 이제 **앱을 실행해서 화면을 확인**한다.

    ### ✔ 실행 방법 예시 (각 웹주소는 Platform links 메뉴 참조)
    - **Google Colab(구글 코랩)**  
      → 코드를 셀에 붙여 실행하며 구조를 이해
    - **로컬 실행(개인 컴퓨터)**  
      ```bash
      streamlit run app.py
      ```

    ### ✔ 실행하면 무엇이 보일까?
    - 브라우저에 자동으로 앱 화면이 열림
    - 버튼을 누르거나 선택지를 바꾸면 **즉시 반응**
    - 코드를 고치면 **화면도 바로 변경**

    ### ✔ 이 단계에서 중요한 경험
    - “내가 만든 코드가 **수업 자료처럼 작동한다**”
    - “학생 입장에서 어떻게 보일까?”를 자연스럽게 상상

    👉 이 단계의 목표는  
    **‘앱이 살아 움직인다’는 감각을 얻는 것**이다.
    """)

# -----------------------------
# Tab 4: 배포하기
# -----------------------------
with tab4:
    st.header("④ 배포하기 (Deploy): 학생들과 공유한다")

    st.markdown("""
    앱을 만들었다면, **학생들과 쉽게 공유**할 수 있어야 한다.

    ### ✔ 배포(Deploy)란?
    - 앱을 인터넷 주소(URL)로 만들어 누구나 접속 가능하게 하는 것 (ex: englishphonetics.streamlit.app)
    - 설치나 로그인 없이 바로 사용

    ### ✔ 대표적인 방법
    - **Streamlit Community Cloud**
    - **Hugging Face Spaces**

    ### ✔ 배포 흐름 (아주 간단히)
    1. GitHub에 코드 업로드
    2. 배포 플랫폼에서 저장소 선택
    3. 자동으로 앱 실행 → URL 생성

    ### ✔ 교육적으로 중요한 점
    - 학생들에게 *“이 링크로 들어오세요”*라고 안내 가능
    - 과제, 실습, 탐구 활동을 **웹 기반으로 제공**

    👉 이 단계는  
    **전공 수업이 교실을 넘어 확장되는 순간**이다.
    """)

# -----------------------------
# Tab 5: 수업 활용
# -----------------------------
with tab5:
    st.header("⑤ 수업 활용: 전공 수업에서 어떻게 쓰일까?")

    st.markdown("""
    Streamlit 앱은 **전공 내용을 담는 그릇**으로 활용할 수 있다.

    ### ✔ 인문 전공 수업 활용 예
    - 개념 정리용 인터랙티브 자료
    - 텍스트 분석·예문 탐색 도구
    - 설문·응답 기반 토론 자료
    - AI 결과를 *직접 조작하며 이해*하는 실습 도구

    ### ✔ 학생에게 어떤 도움이 될까?
    - 수업 내용을 **보면서·경험하면서 이해**
    - 디지털 도구를 *소비자*가 아닌 *활용자*로 경험
    - 전공과 디지털·AI의 연결을 자연스럽게 인식

    ### ✔ 교수자에게 중요한 관점
    - 모든 수업을 바꿀 필요는 없음
    - **하나의 활동, 하나의 과제**부터 시작해도 충분

    👉 Streamlit은  
    **전공 수업을 바꾸기 위한 기술이 아니라,  
    전공 수업을 더 잘 설명하기 위한 도구**이며, 이 과정을 통해 디지털 인공지능 문해력을 높일 수 있다.
    """)

st.markdown("---")
st.caption("© Streamlit 기반 전공 수업 활용 안내 · 초보자용 개요 페이지")
